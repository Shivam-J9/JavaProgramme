package Week12HW.Voting;

public class Arithmetic {

    public static void main(String[] args){
        int a = 25;
        int b = 10;
        System.out.println(a+b);

        int c = 50;
        int d = 39;
        System.out.println(c-d);

        int e = 19;
        int f = 33;
        System.out.println(e*f);

        int g = 38;
        int h = 72;
        System.out.println(g/h);

        int i = 33;
        System.out.println(i++);
        System.out.println(++i);
        System.out.println(--i);
        System.out.println(i--);

    }



}
